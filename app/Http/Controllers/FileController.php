    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {

        $folder_name = $request->prefix ? $request->prefix : $request->key;
        $folders = Storage::disk('s3')->directories($folder_name);
        $files = collect(Storage::disk('s3')->files($folder_name))
            ->map(function ($name) {
                return [
                    'name' => $name,
                    'size' => $this->fileSizeConvert(Storage::disk('s3')->size($name)),
                    'lastModified' => date('Y-m-d H:i:s' ,Storage::disk('s3')->lastModified($name)),
                ];
            });
        return view('files.index', [
            'files' => $files,
            'folders' => $folders
        ]);
    }

    protected function fileSizeConvert($size)
    {
        $result = '';
        $bytes = floatval($size);
        $arBytes = array(
            0 => array(
                "UNIT" => "TB",
                "VALUE" => pow(1024, 4)
            ),
            1 => array(
                "UNIT" => "GB",
                "VALUE" => pow(1024, 3)
            ),
            2 => array(
                "UNIT" => "MB",
                "VALUE" => pow(1024, 2)
            ),
            3 => array(
                "UNIT" => "KB",
                "VALUE" => 1024
            ),
            4 => array(
                "UNIT" => "B",
                "VALUE" => 1
            ),
        );

        foreach ($arBytes as $arItem) {
            if ($bytes >= $arItem["VALUE"]) {
                $result = $bytes / $arItem["VALUE"];
                $result = str_replace(".", ",", strval(round($result, 2))) . " " . $arItem["UNIT"];
                break;
            }
        }
        return $result;
    }

